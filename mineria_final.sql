CREATE DATABASE IF NOT EXISTS mineria_final;

USE mineria_final; 

CREATE TABLE clientes(
	cliente_id INT NOT NULL,
    cli_nombre VARCHAR(50) NOT NULL,
    cli_direccion VARCHAR(50), 
    cli_telefono VARCHAR(10) NOT NULL,
    cli_fecha_ingreso DATETIME,
    PRIMARY KEY(cliente_id) 
);
CREATE TABLE proveedores (
	proveedor_id INT NOT NULL, 
    prov_nombre VARCHAR(50) NOT NULL, 
    prov_direccion VARCHAR(50) NOT NULL, 
    prov_telefono VARCHAR(10) NOT NULL,
    prov_abreviatura VARCHAR(3) NOT NULL,
    PRIMARY KEY(proveedor_id)
);
CREATE TABLE producto(
	producto_id INT NOT NULL,
    proveedor_id INT,
    pro_color VARCHAR(10) NOT NULL, 
    pro_talla INT NOT NULL,
    pro_precio DECIMAL(10,2) NOT NULL,
    PRIMARY KEY (producto_id),
    CONSTRAINT fk_proveedores FOREIGN KEY (proveedor_id) REFERENCES proveedores(proveedor_id)
);
CREATE TABLE entradas(
	entrada_id INT NOT NULL AUTO_INCREMENT,
    cliente_id INT,
    producto_id INT, 
    ent_fecha DATETIME,
    PRIMARY KEY(entrada_id),
    CONSTRAINT fk_clientes FOREIGN KEY (cliente_id) REFERENCES clientes(cliente_id),
    CONSTRAINT fk_producto FOREIGN KEY (producto_id) REFERENCES producto(producto_id)
); 
CREATE TABLE devoluciones(
	devolucion_id INT NOT NULL AUTO_INCREMENT, 
    producto_id INT,
    dev_fecha DATETIME, 
    dev_metodo ENUM('Efectivo','Vales'),
    PRIMARY KEY(devolucion_id),
    CONSTRAINT fk_producto_1 FOREIGN KEY (producto_id) REFERENCES producto(producto_id)
);
CREATE TABLE pedido(
	pedido_id INT NOT NULL, 
    producto_id INT, 
    cliente_id INT,
    ped_fecha DATETIME, 
    PRIMARY KEY(pedido_id),
    CONSTRAINT fk_producto_2 FOREIGN KEY (producto_id) REFERENCES producto(producto_id),
    CONSTRAINT fk_clientes_1 FOREIGN KEY (cliente_id) REFERENCES clientes(cliente_id)
);
CREATE TABLE detalle_pedido(
	detalle_pedido_id INT NOT NULL,
    pedido_id INT, 
    producto_id INT,
    det_precio_total DECIMAL(10,2) NOT NULL,
    PRIMARY KEY (detalle_pedido_id),
    CONSTRAINT fk_pedido FOREIGN KEY (pedido_id) REFERENCES pedido(pedido_id)
);
CREATE TABLE pedido_proveedor(
	pedido_proveedor_id INT NOT NULL,
    proveedor_id INT NOT NULL,
    producto_id INT,
    ord_fecha_realizado DATE, 
    ord_fecha_llegada DATE,
    PRIMARY KEY (pedido_proveedor_id)
);
CREATE TABLE venta(
	venta_id INT NOT NULL AUTO_INCREMENT, 
    cliente_id INT, 
    producto_id INT,
    ven_total DECIMAL(10,2) NOT NULL, 
    ven_fecha DATETIME,
    PRIMARY KEY(venta_id),
    CONSTRAINT fk_clientes_2 FOREIGN KEY (cliente_id) REFERENCES clientes(cliente_id),
    CONSTRAINT fk_producto_5 FOREIGN KEY (producto_id) REFERENCES producto(producto_id)
);
CREATE TABLE vales(
	vales_id INT NOT NULL AUTO_INCREMENT,
    cliente_id INT,
    devolucion_id INT,
    PRIMARY KEY(vales_id),
    CONSTRAINT fk_clientes_3 FOREIGN KEY (cliente_id) REFERENCES clientes(cliente_id),
    CONSTRAINT fk_devoluciones FOREIGN KEY (devolucion_id) REFERENCES devoluciones(devolucion_id)
);
